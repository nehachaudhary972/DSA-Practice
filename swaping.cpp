#include<bits/stdc++.h>
using namespace std;
int main()
{
    cout<<"swapping program"<<endl;
    int a=4,b=7,c;
    cout<<"before swapping"<<endl;
    cout<<"a = "<<a<<endl;
    cout<<"b = "<<b<<endl;
    c=a;
    a=b;
    b=c;
    cout<<"after swapping"<<endl;
    cout<<"a = "<<a<<endl;
    cout<<"b = "<<b<<endl;
    return 0;





}
